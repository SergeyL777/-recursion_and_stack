# Рекурсивная функция для вычисления суммы всех элементов в списке
def sum_list(lst):
    # Базовый случай: если список пустой, сумма равна 0
    if not lst:
        return 0
    # Рекурсивный случай: первый элемент + сумма остальных элементов
    else:
        return lst[0] + sum_list(lst[1:])

# Пример использования
numbers = [1, 2, 3, 4, 5]
print(sum_list(numbers))  # Вывод: 15
print(sum_list([]))  # Вывод: 0
